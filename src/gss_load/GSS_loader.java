    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if(obj.STUDY_PROTOCOL == null || obj.VENDOR == null){
            f=new JFrame();
            JOptionPane.showMessageDialog(f,"Empty Field/s.","Alert",JOptionPane.WARNING_MESSAGE);
        }
        else if (data_load.validateCol(obj.STUDY_PROTOCOL, obj.VENDOR, jComboBox2.getSelectedItem().toString(), getCol4Data(), jComboBox1.getSelectedItem().toString(), jTextField3.getText()).equals("exists")){
            JOptionPane.showMessageDialog(f,"record exists","Alert",JOptionPane.WARNING_MESSAGE);
        }
        else if(!jTextField3.getText().equals(null)){
            data_load.colDataLoad(obj.STUDY_PROTOCOL, obj.VENDOR, null, getCol4Data(), jComboBox1.getSelectedItem().toString(), jTextField3.getText());
        }
        else{
            data_load.colDataLoad(obj.STUDY_PROTOCOL, obj.VENDOR, jComboBox2.getSelectedItem().toString(), getCol4Data(), jComboBox1.getSelectedItem().toString(), jTextField3.getText());
            //jComboBox1.removeItemAt(getCol4Data());
            jTextField3.setText("");
            jTextField3.disable();
            // TODO add your handling code here:
        }
    }//GEN-LAST:event_jButton1ActionPerformed

