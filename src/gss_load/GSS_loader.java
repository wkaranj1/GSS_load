    public  void setDelimiter(){
        if(jComboBox3.getSelectedItem().toString().equals("comma")){
            data_load.setDelimitor(",");
             data_load.addDelimitor(jTextField2.getText(), jTextField1.getText(), "comma", ",");
        }
        
        else if(jComboBox3.getSelectedItem().toString().equals( "pipe")){
            data_load.setDelimitor("|");
            data_load.addDelimitor(jTextField2.getText(), jTextField1.getText(), "pipe", ",");
        }
        else{
             data_load.setDelimitor("-");
             data_load.addDelimitor(jTextField2.getText(), jTextField1.getText(), "hyphen", ",");
        }
            
        
    }
